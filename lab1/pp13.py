txt = 'It\'s alright.'
print(txt)  #It's alright.

txt = "Hello \"World\"!"
print(txt)  #Hello "World"!

txt = "This will insert one \\ (backslash)."
print(txt)  #This will insert one \ (backslash).

txt = "Hello\nWorld!"
print(txt)  #new line

txt = "Hello\rWorld!"
print(txt)  #carriage return

txt = "Hello\tWorld!"
print(txt)  #Hello   World!

txt = "Hello \bWorld!"
print(txt)  #HelloWorld!

txt = "\110\145\154\154\157"
print(txt) #octal value #Hello

txt = "\x48\x65\x6c\x6c\x6f"
print(txt) #hex value #Hello